# Даны строки S, S1 и S2. Заменить в строке S последнее вхождение строки S1 на строку S2.
S = input('Введите строку S: ')
S1 = input('Введите подстроку S1 (которую нужно заменить): ')
S2 = input('Введите подстроку S2 (на которую нужно заменить): ')

# Ищем последнее вхождение подстроки S1
index = S.rfind(S1)

if index == -1:
    # Если S1 нет в строке S — изменений нет
    print('Подстрока S1 не найдена. Строка без изменений:', S)
else:
    # Формируем новую строку:
    #    всё до найденного места
    #  + S2
    #  + всё после S1
    S = S[:index] + S2 + S[index + len(S1):]

    print('Преобразованная строка:', S)
    