# параметры целого типа). С помощью этой функции найти количество и сумму цифр для каждого из пяти данных целых чисел.
def DigitCountSum(K):

    # Функция принимает положительное целое число K и возвращает два значения:
    # C — количество цифр
    # S — сумму цифр

    C = len(str(K))
    S = sum(int(d) for d in str(K))
    return C, S


numbers = []
count = 0

# Запрашиваем 5 чисел с проверкой ввода
while count < 5:
    try:
        K = int(input(f"Введите {count + 1}-е целое положительное число: "))
        if K <= 0:
            print("Ошибка: число должно быть положительным!")
            continue  # возвращаемся к вводу заново
        numbers.append(K)
        count += 1
    except ValueError:
        print("Ошибка: нужно вводить только целые числа!")

# Выводим результаты
print("\nРезультаты:")
for num in numbers:
    C, S = DigitCountSum(num)
    print(f"Число {num}: количество цифр = {C}, сумма цифр = {S}")
