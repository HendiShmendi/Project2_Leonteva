# параметры целого типа). С помощью этой функции найти количество и сумму цифр для каждого из пяти данных целых чисел.
def DigitCountSum(K):
    # Функция принимает положительное целое число K
    # и возвращает количество цифр C и сумму цифр S

    C = 0  
    S = 0  

    # Пока число больше 0
    while K > 0:
        digit = K % 10   # находим последнюю цифру
        S += digit       # добавляем цифру к сумме
        C += 1           # увеличиваем количество цифр
        K //= 10         # убираем последнюю цифру (делим на 10 без остатка)

    return C, S


count = 0  # счётчик введённых чисел

while count < 5:
    try:
        K = int(input(f"Введите {count + 1}-е целое положительное число: "))
        if K <= 0:
            print("Ошибка: число должно быть положительным!")
            continue  # просим ввести заново, если число отрицательное или ноль

        C, S = DigitCountSum(K)
        print(f"Число {K}: количество цифр = {C}, сумма цифр = {S}")
        count += 1

    except ValueError:
        print("Ошибка: нужно вводить только целые числа!")

    