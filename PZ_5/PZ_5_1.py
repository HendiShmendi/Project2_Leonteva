# 1.Найти сумму чисел ряда 1,2,3,4... от числа n до числа m. Суммирование оформить функцией с параметрами. Значения n и m программа должна запрашивать. 
def sum_range(n, m):
    # Функция возвращает сумму чисел от n до m включительно
    if n > m:
        n, m = m, n  # Меняем местами, если n больше m
    return sum(range(n, m + 1))


try:
    n = int(input("Введите число n: "))
    m = int(input("Введите число m: "))
    total = sum_range(n, m)
    print(f"Сумма чисел от {n} до {m} равна {total}")
except ValueError:
    print("Ошибка: необходимо вводить целые числа!")