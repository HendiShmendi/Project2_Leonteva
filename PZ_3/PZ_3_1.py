# Задача 1.Даны числа х, у. Проверить истинность высказывания: «Точка с координатами (х, y) лежит во второй координатной четверти».git init  
try:
    x = float(input("Введите координату x: "))
    y = float(input("Введите координату y: "))

    # Вторая координатная четверть: x < 0 и y > 0
    if x < 0 and y > 0:
        print("Точка лежит во второй координатной четверти.")
    else:
        print("Точка НЕ лежит во второй координатной четверти.")
except ValueError:
    print("Ошибка: введите числовые значения!")
