# Задача 2.Даны координаты поля шахматной доски х, у (целые числа, лежащие в диапазоне 1- 8). Учитывая, что левое нижнее поле доски (1.1) является черным, проверить 
# истинность высказывания: «Данное поле является белым».
try:
    x = int(input("Введите координату x (1-8): "))
    y = int(input("Введите координату y (1-8): "))

    if 1 <= x <= 8 and 1 <= y <= 8:
        # Левое нижнее поле (1,1) — черное.
        # Если сумма координат чётная, поле чёрное, если нечётная — белое.
        if (x + y) % 2 == 0:
            print(f"Поле ({x}, {y}) — черное.")
        else:
            print(f"Поле ({x}, {y}) — белое.")
    else:
        print("Ошибка: координаты должны быть в диапазоне от 1 до 8.")
except ValueError:
    print("Ошибка: координаты должны быть целыми числами!")