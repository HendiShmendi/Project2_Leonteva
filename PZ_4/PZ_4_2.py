# 2. Дано целое число N (>0). Если оно является степенью числа 3, то вывести TRUE,если не является - вывести FALSE.

try:
    # Запрашиваем у пользователя ввод целого числа N
    N = int(input("Введите целое число N (>0): "))
    if N <= 0:
        print("Ошибка: число должно быть больше 0!")
    else:
        x = N
        # Проверяем, делится ли x на 3 без остатка
        # Пока x делится на 3, делим его на 3
        while x % 3 == 0:
            x //= 3
        # Если после делений x стало 1, значит исходное N — степень 3
        if x == 1:
            print("TRUE")
        else:
            # Если x не стало 1, N не является степенью 3
            print("FALSE")
except ValueError:
    print("Ошибка: введите целое число!")   
    